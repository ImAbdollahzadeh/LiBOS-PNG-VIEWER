bool lpe_png_strcmp(const char* str1, const char* str2)
{
	UINT_8 sz = 0;
	while (sz < 4)
	{
		if (str1[sz] != str2[sz])
			return false;
		sz++;
	}
	return true;
}

//***********************************************************************************************************

UINT_32 lpe_string_size(UINT_8* str)
{
	UINT_32 size = 0;
	while (*str++)
		size++;
	return size;
}

